function residuals = objective_function(params, Tacq, My_0, R1A, R1B, DR, FLIP, p_scale, peak_choose)

    params = params.*p_scale;
    kex = params(1); Pa = params(2); deltaA = params(3); deltaB = params(4); R2A = params(5); R2B = params(6);
    
    MA0 = Pa;
    nu_onres = -1./(2*Tacq);
    nu_A = -(nu_onres+deltaA);
    nu_B = -(nu_onres-deltaB);
    omega_A = 2*pi*nu_A;
    omega_B = 2*pi*nu_B;
    
    E1A = exp(-R1A*Tacq);
    E1B = exp(-R1B*Tacq);
    E2A = exp(-R2A*Tacq);
    E2B = exp(-R2B*Tacq);
    
    M_model = M_func(MA0,kex,Tacq,FLIP,DR,R1A,R1B,E1A,E1B,E2A,E2B,omega_A,omega_B,peak_choose);
    % Exact scaling for uniform noise
    scale = (M_model(:)'*My_0(:))/(M_model(:)'*M_model(:));
    residuals = scale*M_model(:) - My_0(:);
end


% lsqnonlin options 
options = optimoptions('lsqnonlin','Display','off','MaxIterations',1000,'TolFun',1e-9,'TolX',1e-9);

% Create a problem for MultiStart
x0_s = lb_s + (ub_s-lb_s)/2;  % single initial guess in middle of bounds (MultiStart will generate others)
problem = createOptimProblem('lsqnonlin', ...
    'x0', x0_s, ...
    'objective', @(p)objective_function(p,Tacq,M_0,R1A,R1B,DR,FLIP,p_scale,peak_choose), ...
    'lb', lb_s, 'ub', ub_s, ...
    'options', options);

npar = numel(lb_s);

% Generate Latin Hypercube samples in [lb,ub]
Xlhs = bsxfun(@plus,lb_s,lhsdesign(Nstart,npar).*(ub_s-lb_s));
startSet = CustomStartPointSet(Xlhs);
ms = MultiStart('Display','off');
rng default
[best_params,fval,exitflag,output,all_solutions] = run(ms, problem, Nstart);
best_params = best_params.*p_scale;

% Extract parameter matrix from all solutions
param_matrix = vertcat(all_solutions.X);  % Each row = one local minimum
fvals = [all_solutions.Fval];             % Objective (resnorm) values

% Keep only finite, successful solutions
valid_idx = isfinite(fvals);
param_matrix = param_matrix(valid_idx,:);
fvals = fvals(valid_idx);

% Plot histograms for each parameter
param_names = {'kex','Population','\Delta\nu_A','\Delta\nu_B','R_2_A','R_2_B'};

figure;
for i = 1:size(param_matrix,2)
    subplot(2,3,i)
    histogram(param_matrix(:,i),15,'FaceColor',[0.2 0.2 0.8],'EdgeColor','k');
    xlabel(param_names{i},'FontSize',12);
    ylabel('Count','FontSize',12);
    title(sprintf('%s (mean=%.3f)',param_names{i},mean(param_matrix(:,i))));
    grid on
end

% Extract optimized parameters
kex_opt = best_params(1);
pa_opt = best_params(2);
cs_A_opt = best_params(3);
cs_B_opt = best_params(4);
R2A_opt = best_params(5);
R2B_opt = best_params(6);

% Compute final fitted signal
nu_onres = -1./(2*Tacq);
nu_A = -(nu_onres+cs_A_opt);
nu_B = -(nu_onres-cs_B_opt);
omega_A = 2*pi*nu_A;
omega_B = 2*pi*nu_B;
E1A = exp(-R1A*Tacq);
E1B = exp(-R1B*Tacq);
E2A = exp(-R2A_opt*Tacq);
E2B = exp(-R2B_opt*Tacq);
MA0 = pa_opt;

M_unscaled = M_func(MA0,kex_opt,Tacq,FLIP,DR,R1A,R1B,E1A,E1B,E2A,E2B,omega_A,omega_B,peak_choose);
scale_factor = (M_unscaled(:)'*M_0(:))/(M_unscaled(:)'*M_unscaled(:));
M_opt = scale_factor*M_unscaled;

% Goodness of fit tests
residuals = M_opt - M_0;
RMSE = sqrt(mean(residuals.^2));

