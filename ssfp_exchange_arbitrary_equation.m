       ' \Delta\nu_{B} = ' num2str(cs_B_opt, '%.3f') ' Hz,',' R2A = ' num2str(R2A_opt, '%.3f'),' s^-1',', R2B = ' num2str(R2B_opt, '%.3f'),' s^-1'])
